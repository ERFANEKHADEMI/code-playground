public class loop {
    public static void main(String[] args) {
        //while 
        int y = 0;
        while (y < 5) {
            System.out.println(y +" : Loop while!");
            y++;
        }

        for (int i = 0; i < 10; i++) {
            System.out.println(i +" Loop for");
        }
    }
}
