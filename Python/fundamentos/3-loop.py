# Loop for: O loop for em Python é usado para iterar sobre uma sequência de elementos.
# Você pode usar o loop for para iterar sobre listas, tuplas, strings, dicionários e outros
# objetos iteráveis. A sintaxe é simples:
#
# for elemento in sequencia:
    # Bloco de código a ser executado para cada elemento na sequência

frutas = ["Banana", "Maça", "Pera", "Goiaba", "Uva", "Manga"]
for key in frutas:
    print(key)

#Loop while: O loop while é usado para executar um bloco de código repetidamente enquanto uma
#condição específica for verdadeira. A sintaxe é:

# while condição:
    # Bloco de código a ser executado enquanto a condição for verdadeira

contador = 0
while contador < 5:
    print(contador)
    contador+= 1