//Math.sqrt(x)
//Retorna a raiz quadrada de x.
const sqrtResult = Math.sqrt(25);
console.log('Square Root:', sqrtResult); // Output: 5

//Math.max(x, y, ...)
//Retorna o maior número entre x, y, e outros numeros.
const maxResult = Math.max(2, 8, 5, 12);
console.log('Maximum:', maxResult); // Output: 12

//Math.round(x)
//Retorna o valor arredondado de x para o inteiro mais próximo.
const roundResult = Math.round(4.5);
console.log('Round:', roundResult); // Output: 5

//Math.floor(x)
//Retorna o maior número inteiro menor ou igual a x.
const floorResult = Math.floor(4.75);
console.log('Floor:', floorResult); // Output: 4

//Math.abs(x)
//Retorna o valor absoluto de x
const absResult = Math.abs(-5);
console.log('Absolute Value:', absResult); // Output: 5

//Math.pow(x, y) 
//Retorna x elevado à potência de y.
const powResult = Math.pow(2, 3);
console.log('Power:', powResult); // Output: 8
