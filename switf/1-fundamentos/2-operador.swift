//2. Operadores de Comparação:
let igual = 5 == 5 // Resultado: true
let diferente = 3 != 4 // Resultado: true
let maiorQue = 7 > 4 // Resultado: true
let menorQue = 2 < 5 // Resultado: true
let maiorOuIgual = 6 >= 6 // Resultado: true
let menorOuIgual = 9 <= 9 // Resultado: true
//***********************************************************************//

//3. Operadores Lógicos:
let and = true && false // Resultado: false
let or = true || false // Resultado: true
let not = !true // Resultado: false
//***********************************************************************//

// 4. Operador Ternário:
let numero = 10
let resultado = numero > 5 ? "Maior que 5" : "Menor ou igual a 5"
// Se o número for maior que 5, resultado será "Maior que 5", senão será "Menor ou igual a 5"
//***********************************************************************//
    
