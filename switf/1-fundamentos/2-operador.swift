//1. Operadores Aritméticos:
let soma = 5 + 3 // Resultado: 8
let subtracao = 7 - 2 // Resultado: 5
let multiplicacao = 4 * 6 // Resultado: 24
let divisao = 10 / 2 // Resultado: 5
let resto = 15 % 4 // Resultado: 3
